def _get_classification_labels(task_name, desc_id):
    """Get classification labels by task_name and the number of classes.

    Parameters
    ----------
    task_name: str
        Task name used __desc__.
    desc_id: str
        As format `DescriptionStr_NumClass` or `NumClass`, separate by `_`,
        ends with number.

    Returns
    -------
    labels: tuple
        The channel labels.
    """

    assert task_name in _cls_task_label_map, (
        "unknown task: %s, register it first." % task_name
    )
    assert isinstance(desc_id, str)
    assert (
        desc_id in _cls_task_label_map[task_name]
    ), "unsupported desc_id %s of task %s, " "register it first." % (
        desc_id,
        task_name,
    )
    num_cls_str = desc_id.split("_")[-1]
    assert num_cls_str.isdigit(), (
        "desc_id should as format `DescriptionStr_NumClass` or `NumClass`,"
        " separate by `_`, ends with number. \n%s" % desc_id
    )
    num_class = int(num_cls_str)

    channel_labels = _cls_task_label_map[task_name][desc_id]
    assert isinstance(channel_labels, (list, tuple))
    assert len(channel_labels) == num_class, "%d vs. %d" % (
        len(channel_labels),
        num_class,
    )

    return channel_labels


def get_rcnn_classification_desc(task_name, output_name, class_name, desc_id):
    """RCNN classification description.

    Parameters
    ----------
    task_name: str
        Task name, for example `frcnn_classification`.
    output_name: str
        Output name, for example `person_age_classification`.
    class_name: str
        The rcnn class name.
    desc_id: str
        The number of classes. Desc id is used to get classification's labels.
    """  # noqa
    assert isinstance(task_name, str)

    channel_labels = _get_classification_labels(output_name, desc_id)
    desc = {
        "task": task_name,
        "output_name": output_name,
        "class_name": class_name,
        "properties": [
            {"channel_labels": channel_labels},
        ],
    }

    desc = json.dumps(desc)
    return desc
